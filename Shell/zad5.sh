
# Да се напише Shell скрипта која како аргументи од командна линија добива имиња на два директориуми,
# притоа доколку се повика без аргументи, скриптата испишува порака за грешка, додека пак, доколку се
# повика со еден аргумент, вториот се смета дека е тековниот директориум. Скриптата треба да ја земе првата
# текстуална датотека од двата директориуми (со екстензија .txt) и да ја повика Python скриптата, со тоа што
# како прв аргумент ќе се прати датотеката од првиот директориум, додека како втор аргумент датотеката од
# вториот директориум. Доколку двата директориуми се исти, тогаш вториот директориум се заменува со
# тековниот директориум. На крај да се отпечати резултатот од извршување на Python скриптата.

#!/bin/sh

prvD=''
# Иницијализира празна променлива prvD (првиот директориум).

vtorD=''
# Иницијализира празна променлива vtorD (вториот директориум).

if [ $# -eq 0 ] then
# Проверува дали бројот на аргументи ($#) е еднаков на 0 (нема аргументи).
# Ако е така, тогаш треба да се изврши наредбата по then.

    exit 'poraka za greska'
    # Ја прекинува скриптата и испишува порака за грешка (но ова е погрешно, exit не печати порака).

elif [ $# -eq 1 ] then
# Ако бројот на аргументи е 1, тогаш изврши го телото.

    prvD=$1
    # Зачува го првиот аргумент во променливата prvD.

    vtorD='.'
    # Вториот директориум го поставува како тековниот директориум ('.').

else 
# Ако има два или повеќе аргументи,

    prvD=$1
    # Зачува го првиот аргумент во prvD.

    vtorD=$2
    # Зачува го вториот аргумент во vtorD.

fi
# Крај на if условот.

if [ $prvD -eq $vtorD ] then
# Проверува дали променливите prvD и vtorD се еднакви. Ова е грешка, бидејќи -eq се користи само за броеви.

    vtorD='.'
    # Ако двата директориуми се исти, вториот се заменува со тековниот директориум.

fi
# Крај на if.

dat1 = $(ls -al $prvD | grep *.txt | head -1)
# Го добива првиот .txt фајл од prvD:
# ls -al $prvD листа содржина,
# grep *.txt филтрира линии кои содржат '*.txt',
# head -1 зема првиот ред.
# Резултатот се зачувува во dat1.
# Но, има проблем со празни места околу '=', треба да се отстранат.

dat2 = $(ls -al $vtorD | grep *.txt | head -1)
# Исто за вториот директориум, ја зема првата .txt датотека.

echo $(zad5.py $dat1 $dat2)
# Ја повикува Python скриптата zad5.py со двата аргументи dat1 и dat2,
# и го испечатува резултатот.
